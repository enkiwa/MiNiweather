def getdate(s):
    url = 'http://www.weather.com.cn/weather/' + city[0] + '.shtml'
    #设置头部信息
    headers={'User-Agent':'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/70.0.3538.110 Safari/537.36'}
    #发送请求
    res = requests.get(url,headers=headers)
    #打印响应状态码
    #print(res.status_code)
    #设置编码方式
    res.encoding = 'utf-8'
    #获取html源码并解析
    soup = BeautifulSoup(res.text,'html.parser')
    body = soup.body
    data = body.find('div',{'id':'7d'})  #找到id = 7d 的div
    ul = data.find('ul')          #此div中只有一个ul
    li = ul.find_all('li')        #此ul中有7个li
    #将天气信息存入列表sevendays备用
    s.append(city[1])
    for day in li:
        date = day.find('h1').string  #日期
        ps = day.find_all('p')
        weather = ps[0].string
        low = ps[1].find('i').string
        if ps[1].find('span') is None:
            high = ''
            temp = low
        else:
            high = ps[1].find('span').string
            temp = high + '/' + low
        #sevendays.append(date)
        s.append(weather)
        s.append(temp)
